// Helper function to get a single blog post by slug
export function getBlogBySlug(slug) {
  return blogPosts.find(post => post.slug === slug)
}

// Helper function to get published blog posts
export function getPublishedBlogs() {
  return blogPosts.filter(post => post.published)
}

// Helper function to get recent blog posts
export function getRecentBlogs(limit = 3) {
  return getPublishedBlogs().slice(0, limit)
}
